public class UnicodeExample {
    public static void main(String[] args) {
        // Unicode for smiley face
        char letter = '\u0041';
        char smiley = '\u263A';
        // Unicode for umbrella
        char umbrella = '\u2602';
        // Unicode for coffee cup
        char coffee = '\u2615';
        // Chinese character
        char chineseChar = '\u9C3F';
        // Indian character
        char indianChar = '\u0905';


        System.out.println("Unicode Characters Example:");
        System.out.println("letter: " + letter);
        System.out.println("Smiley: " + smiley);
        System.out.println("Umbrella: " + umbrella);
        System.out.println("Coffee: " + coffee);
        System.out.println("Chinese Char: " + chineseChar);
        System.out.println("Indian Char: " + indianChar);
    }
}